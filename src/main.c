/**
 * main - Entry point for the SDL-based raycasting engine.
 *
 * Description: Initializes SDL, creates a window, renderer, and texture.
 *              Enters a loop to handle events, perform raycasting, and
 *              update the renderer with the new frame. Handles user input
 *              for movement and quits when the user requests it. Cleans up
 *              resources before exiting.
 *
 * Return: Always returns 0.
 */
int main(void)
{
	int y, x;
	SDL_Window *window = NULL;
	SDL_Renderer *renderer = NULL;
	SDL_Texture *texture = NULL;

	initializeSDL(&window, &renderer, &texture);
	loadTextures();

	double posX = 22.0, posY = 11.5;
	double dirX = -1.0, dirY = 0.0;
	double planeX = 0.0, planeY = 0.66;

	double time = 0;
	double oldTime = 0;

	int running = 1;
	SDL_Event event;

	while (running)
	{
		while (SDL_PollEvent(&event))
		{
			if (event.type == SDL_QUIT)
				running = 0;
			if (event.type == SDL_KEYDOWN && event.key.keysym.sym == SDLK_ESCAPE)
				running = 0;
		}
		/*clean buffer because to prevent texture bleeding*/
		for (y = 0; y < screenHeight; y++)
		{
			for (x = 0; x < screenWidth; x++)
				buffer[y][x] = 0x000000;
		}

		performRaycasting(posX, posY, dirX, dirY, planeX, planeY);
		updateRenderer(renderer, texture, posX, posY, dirX, dirY);

		oldTime = time;
		time = SDL_GetTicks();
		double frameTime = (time - oldTime) / 1000.0;

		handleInput(&posX, &posY, &dirX, &dirY, &planeX, &planeY, frameTime);
	}

	cleanup(texture, renderer, window);
	return (0);
}
